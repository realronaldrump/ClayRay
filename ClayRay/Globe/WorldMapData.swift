    /// Convert lat/lon to equirectangular UV texture coordinates.
    /// Returns (u, v) in 0...1.
    static func latLonToUV(lat: Double, lon: Double) -> (u: Double, v: Double) {
        let u = (lon + 180) / 360
        let v = (90 - lat) / 180
        return (u: u, v: v)
    }

    /// Convert UV texture coordinates back to lat/lon.
    static func uvToLatLon(u: Double, v: Double) -> (lat: Double, lon: Double) {
        let lon = u * 360 - 180
        let lat = 90 - v * 180
        return (lat: lat, lon: lon)
    }
